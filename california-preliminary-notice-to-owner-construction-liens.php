                        <p>If you’re working in the construction industry, then preliminary notice requirements are something you should pay attention to. In California, they changed in July of 2012, so you need to find actual information about them.</p>

                        <p>In this state, in order to retain the ability to file a valid mechanics lien, you need to send a preliminary notice. Property owners, general contractors, and lenders are usually the ones receiving it. They use it to paint a clear picture of the parties that are working on the project.</p>

                        <p>Preliminary notices have a deadline in California, but the state is strict in the required content.</p>

                        <p>The state allows you to file a mechanics lien on a project if you provide labor or materials to it. In some cases, the law specifies that some parties must send preliminary notice to rightfully preserve the mechanics lien.</p>

                        <p>California’s requirements for preliminary notice are not very different from those of other states. Their role is to inform a property owner about a claimant’s right to file a mechanics lien against the property in case there is no payment. A preliminary notice in California is meant to benefit the general contractor, property owner and top project members.</p>

                        <p><strong>What Does a Preliminary Notice Include in California?</strong></p>
                        <ul>
                        <li>The name and address of the construction lender where necessary</li>
                        <li>The owner’s name and address</li>
                        <li>The prime contractor’s name and address</li>
                        <li>The name and address of the party that contracted with the claimant</li>
                        <li>The claimant’s name and address</li>
                        <li>A general statement of the provided work</li>
                        <li>A description of the site that is enough for identification (like a street address)</li>
                        <li>A specific boldface type 235-word statement</li>
                        <li>An estimate of the total price of the future provided work</li>
                        </ul>

                        <p><strong>When Should It Be Sent?</strong></p>
                        <p>A preliminary notice should be sent within 20 days of the first material or labor furnishing.</p>
                        <p>Therefore, in case you send it after 20 days, you should know that it only protects lien rights for the previous 20 days.</p>

                        <p><strong>How Do You Send It?</strong></p>
                        <ul>
                        <li>Preliminary notices can be sent via overnight delivery, express mail or registered mail.</li>
                        <li>Make sure you have proof about mailing your notice.</li>
                        <li>The “certified mail” of the United States Postal Service is the most cost-effective way, as it costs under $5 and you receive a tracking number as well.</li>
                        </ul>

                        <p>California has very good laws when it comes to preliminary notices. However, make sure you do your research before you send one.</p>
